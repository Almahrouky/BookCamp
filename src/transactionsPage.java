    private void refreshButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshButtonActionPerformed
        // TODO add your handling code here:
        try {
            Connection connection = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/library_management_system",
                "root",
                "MHRQ"
            );
            String check = user_idField.getText();
            if(check.equals("")){
                JOptionPane.showMessageDialog(this, "Enter an id");
                return;
            }
            Integer user_id = Integer.parseInt(user_idField.getText());
            
            String query = """
                            select * from borrowed_books
                            where user_id = ?;
                            """;
            PreparedStatement statement = connection.prepareStatement(query);
            statement.setInt(1, user_id);
            ResultSet result = statement.executeQuery();

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);

            while (result.next()) {
                String username = result.getString("username");
                Integer book_id = result.getInt("book_id");
                String status = result.getString("status");
                String queryName = """
                                   select book_name from book where id = ?;
                                   """;
                statement = connection.prepareStatement(queryName);
                statement.setInt(1, book_id);
                ResultSet res = statement.executeQuery();
                String book_name = "";
                while(res.next()){
                    book_name = res.getString("book_name");
                    break;
                }
                model.addRow(new Object[] {username, book_id, book_name, status});
            }

            result.close();
            statement.close();
            connection.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_refreshButtonActionPerformed

