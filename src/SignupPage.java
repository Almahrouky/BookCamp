    private boolean check_username(String username){
        // check length
        if(username.length() < 3 || username.length() > 20){
            JOptionPane.showMessageDialog(this, "Username length should be between 3 and 20 characters");
            return false;
        }
        
        // check characters
        for(int ind = 0; ind < username.length(); ind++){
            if(!Character.isLetterOrDigit(username.charAt(ind)) && username.charAt(ind) != '_'){
                JOptionPane.showMessageDialog(this, "Username should contain only letters, numbers or underscores");
                return false;
            }
        }
        
        // check if already exists
        try {
            Connection connection = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/library_management_system",
                "root",
                "MHRQ"
            );
            
            String query = """
                        select * from user
                        where username = ?;
                        """;
            PreparedStatement statement = connection.prepareStatement(query);
            statement.setString(1, username); // index (1-based), value

            ResultSet result = statement.executeQuery();
            if(result.next()){
                JOptionPane.showMessageDialog(this, "This username is already exists");
                return false;
            }
            
            result.close();
            statement.close();
            connection.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        return true;
    }
    
    private boolean check_password(String password){
        // check length
        if(password.length() < 8 || password.length() > 20){
            JOptionPane.showMessageDialog(this, "Password length should be between 8 and 20 characters");
            return false;
        }
        
        // check password strength
        String spChars = "[!@#$%^&*(),.?\":{}|<>]";
        HashSet<Character> set = new HashSet<>();
        for(int ind = 0; ind < spChars.length(); ind++){
            set.add(spChars.charAt(ind));
        }
        
        boolean findSpChar = false, findUpper = false, findLower = false, findDigit = false;
        for(int ind = 0; ind < password.length(); ind++){
            if(Character.isUpperCase(password.charAt(ind))){
                findUpper = true;
            }
            if(Character.isLowerCase(password.charAt(ind))){
                findLower = true;
            }
            if(Character.isDigit(password.charAt(ind))){
                findDigit = true;
            }
            if(set.contains(password.charAt(ind))){
                findSpChar = true;
            }
        }
        
        if(!findUpper || !findLower || !findDigit || !findSpChar){
            JOptionPane.showMessageDialog(this, "Password should contain at least one uppercase letter, one lowercase letter, one digit, and one special character");
            return false;
        }
        return true;
    }
    
