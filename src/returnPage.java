    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            Integer book_id = Integer.parseInt(ISBNField.getText());
            Connection connection = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/library_management_system",
                "root",
                "MHRQ"
            );
            String query = """
                           update book
                           set copies = ? where id = ?;
                           """;
            
            String getCopies = """
                               select copies from book where id = ?;
                               """;
            PreparedStatement statement = connection.prepareStatement(getCopies);
            statement.setInt(1, book_id);
            ResultSet res = statement.executeQuery();
            Integer oldCopies = 0;
            while(res.next()){
                oldCopies = res.getInt("copies");
                break;
            }
            
            statement = connection.prepareStatement(query);
            statement.setInt(1, oldCopies + 1); // index (1-based), value
            statement.setInt(2, book_id);
            Integer result = statement.executeUpdate();
            
            String trans = """
                           insert into borrowed_books
                           values (?, ?, ?, 'returned');
                           """;
            
            // getting the user id
            String getUser_id = """
                                select id from user where username = ?;
                                """;
            statement = connection.prepareStatement(getUser_id);
            statement.setString(1, username);
            res = statement.executeQuery();
            Integer user_id = 0;
            while(res.next()){
                user_id = res.getInt("id");
                break;
            }
            
            // now execute trans
            statement = connection.prepareStatement(trans);
            statement.setInt(1, user_id);
            statement.setString(2, username);
            statement.setInt(3, book_id);
            result = statement.executeUpdate();
            
            res.close();
            statement.close();
            connection.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed
    
