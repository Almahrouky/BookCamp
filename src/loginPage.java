    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        try {
            Connection connection = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/library_management_system",
                "root",
                "MHRQ"
            );
            String username = usernameField.getText();
            String password = new String(passwordField.getPassword());
            
            String query = """
                select * from user where username = ? and password = ?;
            """;
            PreparedStatement statement = connection.prepareStatement(query);
            statement.setString(1, username); // index (1-based), value
            statement.setString(2, password);
            
            ResultSet result = statement.executeQuery();

            if (result.next()) {
                String type = result.getString("type");
                if(type.equals("admin") || type.equals("owner")){
                    adminPage adminpage = new adminPage();
                    adminpage.setUsernameLabel(username);
                    adminpage.setVisible(true);
                    this.dispose();
                }
                else{
                    DashboardPage dashboard = new DashboardPage();
                    dashboard.setUsernameLabel(username);
                    dashboard.setVisible(true);
                    this.dispose();
                }
                
            }else{
                JOptionPane.showMessageDialog(this, "username or password are incorrect");
            }

            result.close();
            statement.close();
            connection.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_loginButtonActionPerformed

